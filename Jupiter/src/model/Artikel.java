package model;

public class Artikel {

	private int id;
	private String beschreibung;
	private int restmenge;
	private int hersteller_id;
	
	
	
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the beschreibung
	 */
	public String getBeschreibung() {
		return beschreibung;
	}
	/**
	 * @param beschreibung the beschreibung to set
	 */
	public void setBeschreibung(String beschreibung) {
		this.beschreibung = beschreibung;
	}
	/**
	 * @return the restmenge
	 */
	public int getRestmenge() {
		return restmenge;
	}
	/**
	 * @param restmenge the restmenge to set
	 */
	public void setRestmenge(int restmenge) {
		this.restmenge = restmenge;
	}
	/**
	 * @return the hersteller_id
	 */
	public int getHersteller_id() {
		return hersteller_id;
	}
	/**
	 * @param hersteller_id the hersteller_id to set
	 */
	public void setHersteller_id(int hersteller_id) {
		this.hersteller_id = hersteller_id;
	}
	
	
	
	
}
