package model;

public class ArtikelKategorie {

	
	private int artikel_id;
	private int kategorie_id;
	
	
	
	
	
	/**
	 * @return the artikel_id
	 */
	public int getArtikel_id() {
		return artikel_id;
	}
	/**
	 * @param artikel_id the artikel_id to set
	 */
	public void setArtikel_id(int artikel_id) {
		this.artikel_id = artikel_id;
	}
	/**
	 * @return the kategorie_id
	 */
	public int getKategorie_id() {
		return kategorie_id;
	}
	/**
	 * @param kategorie_id the kategorie_id to set
	 */
	public void setKategorie_id(int kategorie_id) {
		this.kategorie_id = kategorie_id;
	}
	
	
}
